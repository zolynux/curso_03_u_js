// Matrices en JS
// Declaración de una matriz

let matriz = [[], []];

// Modificar los valores
// renglón 0
matriz[0][0] = 100;
matriz[0][1] = 200;
matriz[0][2] = 300;

// renglón 1
matriz[1][0] = 400;
matriz[1][1] = 500;
matriz[1][2] = 600;

// leer valores
console.log(`Elemento [0][1] = ${matriz[0][1]}`);
console.log(`Elemento [1][2] = ${matriz[1][2]}`);