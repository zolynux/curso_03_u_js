// Operadores Lógicos
let a = true;
let b = false;
console.log(a);
console.log(b);

// Operadore Logic && (AND o Y)
// Regresa verdadero si ambos operandos
// Son verdaderos
console.log(`${a} && ${b} -> ${a && b}`);

// Operadore Logic || (OR o Ó)
// Regresa verdadero si cualquiera de los valores es verdadero
console.log(`${a} || ${b} -> ${a || b}`);

// Operadore Logic ! (NOT o NO)
// Invierte el valor original, true -> false, false -> true
console.log(`${a}  -> ${!a}`);

