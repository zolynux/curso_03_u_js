// Operadores Aritmeticos
let a, b, c, d, e, f;

// suma
a = 3 + 4;
console.log(a);

// resta
b = 6 - 2;
console.log(b);

// Multiplicación
c = a * 2
console.log(c);

d = b / 2.5;
console.log(d);
console.log(typeof d);

// Módulo (residuo de la división)
f = 9 % 2;
console.log(f);

// Potencia (ES2016, ECMAscript 2016)
e = 2 ** 3; // 2 * 2 * 2 = 8
console.log(e);

e = Math.pow(2, 3);
console.log(e);
